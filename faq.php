      <h3>When can I submit a proposal/request?</h3>
      <p>For the first year, application process will begin on February 8, and will be running until March 31, 2017.  For subsequent years, the application dates will be announced.</p>

      <h3>What information do I have to provide?</h3>
      <p>You must include all the information and documents that provide information and support your proposal request or proposal cost. If applicable, please provide an updated quote for the items that you are requesting, spec sheets or link where the information can be founded as well as a written approval from IT or Facilities departments if needed.</p>
      <p>Other information as your email address, name, department will be requested in order to create your profile.</p>

      <h3>Do I need to let my supervisor know what I am applying for?</h3>
      <p>We h3ly encourage you to have a conversation with your supervisor before you apply to DavenFund.  Please consider that if you get selected for a funding to a training conference for example, this may impact your department or colleague’s workloads, so you may need to identify the best date for your attendance.</p>
      <h3>When is the final selection date?</h3>
      <p>For the first year the selection process will occur on May 15, 2017.</p>
      <h3>How does the final selection occur?</h3>
      <p>It is a random selection that will be live-streamed through DavenFund website for year one. <p>For subsequent Fiscal Years, we are hoping to have the random selection occur at Davenfest. Once the winners have been identified, an announcement will go out to all participants. If your proposal was not selected you can resubmit it for the following Fiscal Year.</p>
      <h3>When is the review process? What happens during the review process?</h3>
      <p>The review process is facilitated by the DavenFund Committee. This committee will be in responsible for reviewing all the proposals to be sure they meet the criteria and have all the needed information included with submission.</p>
      <p>This process will happen after the application time is closed, beginning April 1 through May 1, 2017 for the first year.</p>
      <h3>What kind of things would I need to get Facilities approval for?</h3>
      <p>According to DU policies any service required for maintenance, repairs, construction, renovation or modification of any DU facilities must be reviewed and approved by the Facilities Staff.</p>
      <h3>What kind of things would I need IT’s approval for?</h3>
      <p>According to DU policies, any requests for software, audio/video technology or IT related must be analyzed and approved by IT Staff.</p>
      <h3>What happens if my proposal get selected?</h3>
      <p>Process Improvement will complete the purchase for your training, equipment or other approved funding request no later than May 31st. In the event that you leave DU, all equipment, software or other tangible items will continue to be owned by DU. For any employee that has been awarded attendance at a conference/training and leaves DU employment prior to attending, the committee reserves the right to re-evaluate the award.</p>
      <h3>How to measure the impact that the request will have on my performance?</h3>
      <p>An estimated improvement must be explained on the application. This can be based on your experience and expectations. A post-award evaluation on how the funding has helped you or your department will take place six months after the funded item or activity has been completed, including a summary of how the funding through DavenFund has positively impacted your performance at DU.</p>
