      <h3>Who is eligible to apply to DavenFund?</h3>
      <p>If you are a Full time or Part time DU employee, you may submit your proposals during the application acceptance period from February 8, 2017 through March 31, 2017. Only one application per person will be considered.</p>
      <p>If you are an adjunct, student worker, temporary or contracted staff, you are not eligible to participate in DavenFund. However, there are some other funds and resources that you can use to improve your performance at DU.</p>
